function safeText(value: unknown, fallback = "—") {
  if (typeof value !== "string") return fallback;
  const trimmed = value.trim();
  return trimmed || fallback;
}

function safePdfText(value: unknown, fallback = "—") {
  const text = safeText(value, fallback);

  return text
    .replace(/₹/g, "INR ")
    .replace(/[^\x00-\x7F]/g, "");
}

function formatCurrency(amount: unknown) {
  if (typeof amount !== "number" || !Number.isFinite(amount)) return "Not set";

  return `INR ${new Intl.NumberFormat("en-IN", {
    maximumFractionDigits: 0,
  }).format(amount)}`;
}

const APP_TIME_ZONE = "Asia/Kolkata";

function formatDate(value: unknown) {
  if (!value) return "—";

  const date = new Date(value as any);

  if (Number.isNaN(date.getTime())) return "—";

  return date.toLocaleString("en-IN", {
    timeZone: APP_TIME_ZONE,
    day: "numeric",
    month: "short",
    year: "numeric",
    hour: "numeric",
    minute: "2-digit",
    hour12: true,
    timeZoneName: "short",
  });
}

function getPdfGeneratedAt(agreement: AgreementLike) {
  return (
    agreement.pdfGeneratedAt ||
    agreement.signedAt ||
    agreement.createdAt ||
    new Date()
  );
}

function splitTextIntoLines(text: string, maxChars: number) {
  const words = text.replace(/\s+/g, " ").trim().split(" ");
  const lines: string[] = [];
  let current = "";

  for (const word of words) {
    const next = current ? `${current} ${word}` : word;

    if (next.length > maxChars) {
      if (current) lines.push(current);
      current = word;
    } else {
      current = next;
    }
  }

  if (current) lines.push(current);

  return lines.length ? lines : ["—"];
}

function drawTextBlock({
  page,
  text,
  x,
  y,
  size,
  font,
  color = DARK,
  maxChars = 90,
  lineHeight = LINE_HEIGHT,
}: any) {
 const lines = splitTextIntoLines(safePdfText(text), maxChars);
  let currentY = y;

  for (const line of lines) {
    page.drawText(line, {
      x,
      y: currentY,
      size,
      font,
      color,
    });

    currentY -= lineHeight;
  }

  return currentY;
}

function drawSectionTitle({ page, title, x, y, font }: any) {
  page.drawText(title, {
    x,
    y,
    size: 10,
    font,
    color: BRAND_ORANGE,
  });

  return y - 18;
}


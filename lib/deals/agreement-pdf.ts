type AgreementLike = {
  agreementNumber?: string;
  status?: string;
  signedAt?: Date | string | null;
  pdfGeneratedAt?: Date | string | null;
  createdAt?: Date | string | null;
  snapshot?: {
    title?: string;
    description?: string;
    proposedAmount?: number;
    finalAmount?: number | null;
    paymentStatus?: "unpaid" | "pending" | "paid";
    message?: string;
    notes?: string;
    deliverables?: string[];
    organizer?: {
      name?: string;
      companyName?: string;
      email?: string;
      phone?: string;
    };
    sponsor?: {
      name?: string;
      companyName?: string;
      email?: string;
      phone?: string;
    };
    event?: {
      title?: string;
      location?: string;
      startDate?: string | null;
    };
  };
  organizerVerification?: {
    otpStatus?: string;
    otpVerifiedAt?: Date | string | null;
  };
  sponsorVerification?: {
    otpStatus?: string;
    otpVerifiedAt?: Date | string | null;
  };

  proofFiles?: Array<{
    label?: string;
    fileUrl?: string;
    fileType?: string;
    transactionId?: string;
    paidAmount?: number | null;
    paymentDate?: Date | string | null;
    paymentMode?: string;
    note?: string;
    status?: string;
    uploadedAt?: Date | string | null;
  }>;
};

